import { eq, desc } from "drizzle-orm";
import { drizzle } from "drizzle-orm/mysql2";
import { InsertUser, users, demoSubmissions, InsertDemoSubmission, DemoSubmission } from "../drizzle/schema";
import { ENV } from './_core/env';

let _db: ReturnType<typeof drizzle> | null = null;

// Lazily create the drizzle instance so local tooling can run without a DB.
export async function getDb() {
  if (!_db && process.env.DATABASE_URL) {
    try {
      _db = drizzle(process.env.DATABASE_URL);
    } catch (error) {
      console.warn("[Database] Failed to connect:", error);
      _db = null;
    }
  }
  return _db;
}

export async function upsertUser(user: InsertUser): Promise<void> {
  if (!user.openId) {
    throw new Error("User openId is required for upsert");
  }

  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot upsert user: database not available");
    return;
  }

  try {
    const values: InsertUser = {
      openId: user.openId,
    };
    const updateSet: Record<string, unknown> = {};

    const textFields = ["name", "email", "loginMethod"] as const;
    type TextField = (typeof textFields)[number];

    const assignNullable = (field: TextField) => {
      const value = user[field];
      if (value === undefined) return;
      const normalized = value ?? null;
      values[field] = normalized;
      updateSet[field] = normalized;
    };

    textFields.forEach(assignNullable);

    if (user.lastSignedIn !== undefined) {
      values.lastSignedIn = user.lastSignedIn;
      updateSet.lastSignedIn = user.lastSignedIn;
    }
    if (user.role !== undefined) {
      values.role = user.role;
      updateSet.role = user.role;
    } else if (user.openId === ENV.ownerOpenId) {
      values.role = 'admin';
      updateSet.role = 'admin';
    }

    if (!values.lastSignedIn) {
      values.lastSignedIn = new Date();
    }

    if (Object.keys(updateSet).length === 0) {
      updateSet.lastSignedIn = new Date();
    }

    await db.insert(users).values(values).onDuplicateKeyUpdate({
      set: updateSet,
    });
  } catch (error) {
    console.error("[Database] Failed to upsert user:", error);
    throw error;
  }
}

export async function getUserByOpenId(openId: string) {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot get user: database not available");
    return undefined;
  }

  const result = await db.select().from(users).where(eq(users.openId, openId)).limit(1);

  return result.length > 0 ? result[0] : undefined;
}

// Demo submission helpers

export async function createDemoSubmission(submission: InsertDemoSubmission): Promise<DemoSubmission | null> {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot create demo submission: database not available");
    return null;
  }

  try {
    await db.insert(demoSubmissions).values(submission);
    
    // Get the newly created submission
    const result = await db
      .select()
      .from(demoSubmissions)
      .where(eq(demoSubmissions.email, submission.email))
      .orderBy(desc(demoSubmissions.createdAt))
      .limit(1);
    
    return result.length > 0 ? result[0] : null;
  } catch (error) {
    console.error("[Database] Failed to create demo submission:", error);
    throw error;
  }
}

export async function getDemoSubmissions(): Promise<DemoSubmission[]> {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot get demo submissions: database not available");
    return [];
  }

  return db.select().from(demoSubmissions).orderBy(desc(demoSubmissions.createdAt));
}

export async function getDemoSubmissionById(id: number): Promise<DemoSubmission | null> {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot get demo submission: database not available");
    return null;
  }

  const result = await db.select().from(demoSubmissions).where(eq(demoSubmissions.id, id)).limit(1);
  return result.length > 0 ? result[0] : null;
}

export type DemoSubmissionStatus = "new" | "contacted" | "converted" | "closed";

export async function updateDemoSubmissionStatus(
  id: number, 
  status: DemoSubmissionStatus
): Promise<DemoSubmission | null> {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot update demo submission: database not available");
    return null;
  }

  try {
    await db
      .update(demoSubmissions)
      .set({ status })
      .where(eq(demoSubmissions.id, id));
    
    return getDemoSubmissionById(id);
  } catch (error) {
    console.error("[Database] Failed to update demo submission status:", error);
    throw error;
  }
}

export async function deleteDemoSubmission(id: number): Promise<boolean> {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot delete demo submission: database not available");
    return false;
  }

  try {
    await db.delete(demoSubmissions).where(eq(demoSubmissions.id, id));
    return true;
  } catch (error) {
    console.error("[Database] Failed to delete demo submission:", error);
    throw error;
  }
}
